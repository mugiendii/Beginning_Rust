fn main() {
    // Signed integer
    let x: i32 = -42;
    // Unsigned integer
    let y: u64 = 314;
    // Floating-point number
    let pi: f64 = 3.14;
    //boolean
    let is_rust_fun: bool = true;
    //character
    let letter: char = 'R';
    println!("signed: {}, unsigned: {}, floating-point: {}, boolean: {}, alphabet: {}", x, y, pi, is_rust_fun, letter);
}
